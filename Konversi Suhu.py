Angka= int(input('Masukkan suhu dalam celcius '))

konversi_reamur= Angka * 0.8
konversi_fahrenheit= Angka * 1.8 + 32
konversi_kelvin= Angka + 273

print(Angka, '⁰C dalam reamur menjadi', konversi_reamur, '⁰R dan dalam fahrenheit menjadi', konversi_fahrenheit, '⁰F dan dalam kelvin menjadi', konversi_kelvin, '⁰K')